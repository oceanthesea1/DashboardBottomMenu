package com.example.dashboardbottommenu;

public class Item {

    String shape, image;

    public Item() {
        this.shape = shape;
        this.image = image;
    }

    public String getShape() {
        return shape;
    }

    public void setShape(String shape) {
        this.shape = shape;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
